<template>
  <div>
    <h2 class="font-weight-light">
      Contador: {{ contador }}
      <br>
      <button class="btn btn-lg btn-danger mr-1" @click="decrementar">-</button>
      <button class="btn btn-lg btn-success" @click="incrementar">+</button>
    </h2>
  </div>
</template>
<script>
export default {
  name: 'contador-click',
  data () {
    return {
      contador: 0
    }
  },
  methods: {
    decrementar () {
      this.contador--
    },
    incrementar () {
      this.contador++
    }
  }
}
</script>
